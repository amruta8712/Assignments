package SpringMVC.Que2;

public class SI {
	long PA;
	int NY;
	double RI;
	public long getPA() {
		return PA;
	}
	public void setPA(long pA) {
		PA = pA;
	}
	public int getNY() {
		return NY;
	}
	public void setNY(int nY) {
		NY = nY;
	}
	public double getRI() {
		return RI;
	}
	public void setRI(double rI) {
		RI = rI;
	}
	
}